class leito:

# *******************************************************************************
# *******************************************************************************
	def __init__(self, id, categoria, hospital):

		self.__id__  	   = id
		self.__categoria__ = categoria
		self.__hospital__  = hospital

# *******************************************************************************
# *******************************************************************************
	def __str__(self): 
		return "Leito: {} | Hospital: {}".format(self.__id__, self.__hospital__)

# *******************************************************************************
# *******************************************************************************
	def get_id(self):
		return self.__id__

# *******************************************************************************
# *******************************************************************************
	def get_categoria(self):
		return self.__categoria__

# *******************************************************************************
# *******************************************************************************
	def get_hospital(self):
		return self.__hospital__



